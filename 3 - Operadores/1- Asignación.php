<?php 

// --------------------------------
// -- Operador de asignación ~ +=, -=, %=, *=, /=, **=
// --------------------------------

$A = 220;
$B = 180;

echo "A: $A" . PHP_EOL;
echo "B: $B" . PHP_EOL;

$A += $B; // $A = $A + $B;
echo "A+B: $A" . PHP_EOL;
$A -= $B; // $A = $A - $B;
echo "A-B: $A" . PHP_EOL;